'use strict';

// JS defines some shorthand arithmetic operators

let count = 0; // Define a var
count++; // Increment the var
count--; // Decrement the var
count += 2; // Add 2: same as count = count + 2
count *= 3; // Multiply by 3: same as count = count * 3
count; // => 6: var names are expressions, too.
console.log(count);
