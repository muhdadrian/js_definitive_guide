// String padding functions in ES2017
console.log('x'.padStart(3)); //add spaces on the left to a length of 3
console.log('x'.padEnd(3)); //add spaces on the right to a length of 3
console.log('x'.padStart(3, '*')); //add stars on the left to a length of 3
console.log('x'.padEnd(3, '-')); //add dashes on the right to a length of 3
