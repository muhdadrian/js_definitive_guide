'use strict';

// JS types can be divided into two categories:
// 1. primitive types
// 2. object types

// primitive types: numbers, strings and booleans
// the special JS values null and undefined are primitive values, but they are not numbers, strings, or booleans.

// ES6 adds a new special-purpose type, known as Symbol, that enables the definition of language extensions without harming backward compatibility.

// Any JS value that is not a number, a string, a boolean, a symbol, null, or undefined is an object.
// An object is a collection of properties where each property has a name and a value (either a primitive value or another object).
// An ordinary JS object is an unordered collection of named values.
// The language also defines a special kind of object, known as an array, that represents an ordered collection of numbered values.
// JS language includes special syntax for working with arrays, and arrays have some special behaviour that distinguishes them from ordinary objects.
// Like any JS value that is not a primitive value, functions and classes are a specialized kind of object.

// a.sort(); // The object-oriented version of sort(a).

// JS's object types are mutable and its primitive types are immutable.
