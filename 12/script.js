'use strict';

// When functions are assigned to the properties of an object, we call them 'methods'. All JS objects (including arrays) have methods:

let a = []; // Create an empty array
console.log(a);

a.push(1, 2, 3); // The push() method adds elements to an array
console.log(a);

a.reverse(); // Another method: reverse the order of elements
console.log(a);
